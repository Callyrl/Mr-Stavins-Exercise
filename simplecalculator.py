firstNum = input("Enter your number:")
secNum = input("Enter your number:")

def addition(num_1, num_2):
    return int(num_1) + int(num_2)
    
print(addition(firstNum, secNum))

def substraction(num_1, num_2):
    return int(num_1) - int(num_2)

print(substraction(firstNum, secNum))

def multiply(num_1, num_2):
    return int(num_1) * int(num_2)

print(multiply(firstNum, secNum))

def divide(num_1, num_2):
    return int(num_1) / int(num_2)

print(divide(firstNum, secNum))
